    <p>Notre histoire commence en 2010, lorsque notre fondateur, Jean Forteroche, décide de créer une association pour la protection des animaux. Il a toujours été passionné par les animaux et a décidé de consacrer sa vie à leur protection. Il a commencé par recueillir des animaux abandonnés et maltraités, puis a ouvert un refuge pour les accueillir. Depuis, notre association n'a cessé de grandir et de se développer. Aujourd'hui, nous avons plusieurs refuges à travers le pays et nous venons en aide à des milliers d'animaux chaque année. Notre mission est de protéger et de défendre les animaux, de leur offrir une vie meilleure et de sensibiliser le public à la cause animale. Nous croyons que chaque animal a le droit d'être traité avec respect et dignité, et nous nous engageons à lutter contre la maltraitance et l'abandon des animaux. Rejoignez-nous dans notre combat pour un monde meilleur pour les animaux !</p>
    
